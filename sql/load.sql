    cost,
    payment_method_id,
    ean13_code
)
SELECT
    ((gs - 1) % 23) + 1 AS event_id,
    CASE
        WHEN gs % 5 = 0 THEN NULL
        ELSE ((gs - 1) % 200) + 1
    END AS visitor_id,
    CASE
        WHEN gs % 5 = 0 THEN NULL
        ELSE (DATE '2025-04-01' - ((gs % 30) * INTERVAL '1 day'))
    END AS purchase_date,
    ((gs - 1) % 3) + 1 AS ticket_category_id,
